# Tipo de dado bool (booleans)
# Ao questionar algo em um programa
# podemos ter apenas duas respostas possíveis: Verdadeiro ou Falso (True ou False)
#Exiostem varios operadores para questionar
# Igualdade: == que é usado para comparar se um valor é igual a outro
print(10 == 10)  # Retorna True
print(10 == 5)   # Retorna False
