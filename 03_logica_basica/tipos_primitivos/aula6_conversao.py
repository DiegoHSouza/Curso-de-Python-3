#conversão de tipos, coerção
#type convertion, typecastingm coercion
# é o ato de converter um tipo de dado em outro
# tipos imutáveis e primitivos:
# int (inteiros)
# float (números de ponto flutuante)
# bool (booleanos)
# str (strings ou textos)

print(int('1') + 1) #converte str para int
print(type(float('1'))) #converte str para float
print(bool(1)) #converte int para bool
print(str(11) + 'b') #converte int para str

