# Tipos int e float
# int = inteiro
# O tipo int representa qualquer número inteiro, positivo ou negativo, sem parte decimal.
# Positivo
print(10)
print(-10)
print(0)

#float = ponto flutuante
# O tipo float representa números reais, ou seja, números que possuem parte decimal.
#float sem sinal é considerado positivo
# Float
print(10.5)
print(-10.5 , -0.00) # Virgula serve para separar valores
print(0.0)

# A função type() mostra o tipo do valor ou variável
print(type('Diego Souza')) # str
print(type(10))            # int
print(type(10.5))          # float  
# type é uma classe e não uma função, por isso a sintaxe sem parênteses na definição